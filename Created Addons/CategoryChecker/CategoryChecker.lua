local function AppendSpellCategoryToTooltip(self)
    local name, link = self:GetItem()
    if not link then return end
    
    local itemID = tonumber(link:match("item:(%d+)"))
    if not itemID then return end
    
    local categoryID = ItemCategoryCache[itemID]
    if categoryID == nil then
        -- C_Item.GetItemSpell return signature shifted across client versions
        local val1, val2, val3
        if C_Item and C_Item.GetItemSpell then
            val1, val2, val3 = C_Item.GetItemSpell(itemID)
        else
            val1, val2, val3 = GetItemSpell(itemID)
        end
        
        local spellID = nil
        if type(val3) == "number" then
            spellID = val3
        elseif type(val2) == "number" then
            spellID = val2
        end
        
        categoryID = (spellID and SpellCategoryDB[spellID]) or false
        ItemCategoryCache[itemID] = categoryID
    end
    
    if categoryID then
        local lineText = "CategoryID: " .. categoryID
        local lineExists = false
        
        -- Scan existing lines to avoid duplicate injection on tooltip refresh
        for i = 1, self:NumLines() do
            local line = _G[self:GetName() .. "TextLeft" .. i]
            if line and line:GetText() == lineText then
                lineExists = true
                break
            end
        end
        
        if not lineExists then
            self:AddLine(lineText, 0.8, 0.4, 0.0)
            self:Show()
        end
    end
end

GameTooltip:HookScript("OnTooltipSetItem", AppendSpellCategoryToTooltip)
ItemRefTooltip:HookScript("OnTooltipSetItem", AppendSpellCategoryToTooltip)